// minha calculadora

let numero1 = 80;
let numero2 = 90;
let sinal = "-";
let resultado;
if (sinal === "+") {
    console.log(resultado = numero1 + numero2);
}
else if (sinal === "-") {
    console.log(resultado = numero1 - numero2)
}
else if (sinal === "*") {
    console.log(resultado = numero1 * numero2);
}
else if (sinal === "/") {
    console.log(resultado = numero1 / numero2)
}
else {
    console.log(resultado = "O seu resultado é inválido!")
}